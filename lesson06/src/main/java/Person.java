public class Person {
    private int age;
    private String name;
    private String sex;
    private int countKids;

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public int getCountKids() {
        return countKids;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public void setCountKids(int countKids) {
        this.countKids = countKids;
    }
}
